            <br><i>Как начать зарабатывать в партнёрской программе?</i>
            <p>В разделе «Смарт-линки», Нажмите "Создать смарт-линк", выберите тематику вашего трафика и тип выплаты вознаграждения - за ребилл или за подписку. Затем, кликая на офферах, выберите по одному или по нескольку офферов для нужных вам операторов. Нажмите - "Продолжить". <br>

            На втором шаге укажите для выбранных офферов - вес (в нижнем правом углу карточки оффера). Чем больше вес оффера - тем больше вероятность его  отображения абоненту. Укажите название вашего Смарт-линка. Если требуется, укажите урл для отправки уведомлений на Postback  и урл для отправки неподходящего трафика на Traffickback. Нажмите "Получить ссылку". <br>

            Разместите полученную ссылку на своем сайте, в бирже или ином источнике трафика и следите за обновлением статистики! <br>

            Подробная пошаговая инструкция с картинками <a href="#">тут</a></p>
            

            <br><i>Смарт-линки</i>
            <p>Функционал смарт-линка позволяет получить единую ссылку для продвижения ряда офферов одной тематики по нескольким операторам. При создании смарт-линка используйте фильтры "Огонь" для Топовых офферов и "Новьё" для новых офферов, либо обратитесь за рекомендациями по интересным офферам к вашему Аккаунт-менеджеру. Подберите по два-три оффера на каждого оператора. Используйте параметр "вес" для указания - какой оффер будет показываться в первую очередь. При повторном переходе, Абоненту будет отображаться каждый раз новый лендинг, так как Смарт-линк запоминает пользователей, которые однажды уже переходили по его ссылке. <br>
            
            Эффективнее всего монетизировать трафик, направляя абонента на набор офферов, отображая их поочередно, в порядке уменьшения конверта лендингов.</p>

            <br><i>Потоки</i>
            <p>Если вы собираетесь продвигать оффер через несколько источников трафика и хотите видеть отдельную статистику по каждому из источников, добавьте/укажите дополнительные Потоки и сгенерируйте для них отдельные ссылки.</p>
            
            <br><i>Trafficback</i>
            <p>Если вы хотите перенаправлять всех пользователей, которые не подходят под условия офферов вашей ссылки, по которой они перешли из рекламной сети, на альтернативный URL, укажите в поле Traffick back URL, на который система перенаправит ваших пользователей. Это могут быть пользователи, не попадающие в требуемые ГЕО или пользователи, перешедшие по ссылке с устройства, которое не входит в список разрешенных оператором.</p>

            <br><i>Postback</i>
            <p>Если вы хотите получать информацию о всех свершившихся по офферу событиях/транзакциях в свою собственную систему статистики, укажите ваш Postback URL, на который от нас  будут приходить нотификации согласно данному <a href="#">API.</a></p>

