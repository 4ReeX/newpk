<br><i>Что такое Вапклик</i>
<p>Вапклик (wapclick,1click) - это оплата контента на сайтах (видео, музыки, файлов) в один клик (без ввода номера телефона и кода подтверждения). Для оплаты абоненту достаточно нажать одну кнопку (Ок, загрузить, подтвердить, подписаться).</p>

<br><i>Как зарабатывать на Вапклик</i>
<p>Абоненты переходят по вашей уникальной ссылке, активируют подписку и Оператор ежедневно начинает списывать деньги с их счета до тех пор, пока Абоненты не отключат подписку. Вы получаете отчисления с каждого списания.</p>

<br><i>В чем преимущества вапклика?</i>
<p>- Высокий конверт. Для активации подписки Абоненту нужно сделать всего один клик. <br>
- Хороший доход. Активированная подписка приносит деньги в течении нескольких месяцев. <br>
- Схема работы, полностью согласованная с Оператором.</p>

<br><i>Как это работает? (Пример с МТС)</i>
<p>Пользователь заходит на сайт с мобильного телефона через сеть мобильного оператора. Кликает на  понравившийся контент. <br>

Наш скрипт определяет IP адрес Абонента, если он принадлежит одному из операторов, которых мы принимаем (см. список поддерживаемых IP адресов), то скрипт показывает Абоненту лендинг. <br>

Если Абонент нажимает на лендинге кнопку Ок (Далее, Продолжить, Получить и т.п.), то у него активируется подписка, и начинают списываться деньги. <br>

Для пользователей, которые заходят на сайт через Wi-Fi переадресация не срабатывает.</p>

<br><i>Что такое лендинг? (ЛП, кастом)</i>
<p>Лендинг - это страница подтверждения, размещенная на стороне Оператора, на которую попадает пользователь, переходя по вашей рекламной ссылке. <br>
На лендинге размещена стоимость подписки и описание условий ее предоставления.</p>

<br><i>Стандартный лендинг</i>
<p>Стандартный лендинг оператора - простой белый шаблон с текстом описания услуги и ее стоимости. <br>
Иногда на нем может присутствовать капча и поле для ввода номера телефона Абонента. </p>

<br><i>Кастомный лендинг (кастом)</i>
<p>Кастомный лендинг – привлекательно оформляется в стиле тематики сайта с контентом, кнопка согласия на нем большая и заметная, а правила оказания услуги - не выделяются. <br>
По согласованию с Оператором можно использовать разные варианты лендингов чтобы добиться повышения конверта.</p>

<br><i>Прелендинг (прокладка)</i>
<p>Страница-прокладка, на которую попадает абонент перед тем как попасть на лендинг. <br>
Включается в некоторых случаях для абонентов МТС и Мегафон для снижения рисков нарушений правил привлечения трафика.</p>

<br><i>Какие операторы поддерживаются?</i>
<p>На данный момент мы принимаем трафик по операторам РФ: МТС, Билайн, Теле2, Мегафон и СНГ: Украина, Азербайджан, Казахстан и Беларусь.</p>

<br><i>Какие устройства  и платформы принимаются?</i>
<p>Вапклик работает практически со всеми мобильными устройствами, начиная от кнопочных телефонов, заканчивая современными смартфонами и iPhone, единственное условие – Абонент должен переходить по рекламной ссылке с мобильного Интернета (wi-fi должен быть выключен!). <br>
Билайн - принимаются ТОЛЬКО телефоны, другие устройства (планшеты,usb-модемы, iPod) оператор не разрешает подписывать.<br>
МТС - принимаются телефоны и Android-планшеты. USB-модемы, IPAD, iPod) оператор не разрешает подписывать.<br>
Теле2 - принимаются все устройства, телефоны, планшеты, модемы и т.д.</p>

<br><i>Сколько в среднем живет подписка?</i>
<p>Срок жизни подписки зависит от оператора, устройства абонента, возраста, пола, региона местонахождения абонента, времени момента активации подписки, и других. <br>
Подписка будет активна до того момента пока абонент ее не отключит самостоятельно, или его не отпишет оператор по жалобе абонента или не пройдет период истечения давности подписки (после 30 дней неудачных списаний – подписка закрывается оператором).</p>

<br><i>Какой доход приносит подписка?</i>
<p>В среднем, подписка за время жизни приносит от 25 до 65 рублей в зависимости от цены и оператора.</p>

<br><i>Лендинги какой стоимости показывают лучший конверт?</i>
<p>При подписке чаще всего происходят ежедневные списания. Обычно стоимость для абонента 10, 20 или 30 рублей в сутки.</p>

<br><i>Как система отличит моих подписчиков?</i>
<p>Для каждого партнера наша система присваивает уникальный идентификатор. Все активированные подписки помечаются этими идентификаторами. <br>
После активации подписки, все уведомлении о списаниях денег по этой подписке будут записываться в статистику Партнера, которому она принадлежит.</p>

<br><i>Какие ресурсы-источники трафика допускаются для работы в партнерской программе?</i>
<p>Любые интернет-ресурсы, в том числе мобильные сайты, содержание которых не противоречит законодательству РФ.</p> <br><br><br>
