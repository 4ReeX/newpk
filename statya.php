Самый адекватный вариант теста дохода в партнерке: 

 ПП дает вам некоторую базовую ставку выкупа и капу (количество лидов), которую ПП от вас готова принять для оценки привлекаемого вами трафика.

По результатам оценки качества трафика (ребилльности и lifetime) ставка выкупа может быть пересмотрена как в меньшую (при низкой ребилльности) так и в большую сторону.  На этом пункте вы можете засомневаться, но поверьте, качественный трафик нужен всем  и только алчная и недалекая ПП не даст вам повышенную ставку. 

И помните – этот процесс должен быть win-win, не стоит пытаться обмануть ПП: у них больше данных о качестве вашего трафика)
 
 
Воображаемые «ужасы» и предубеждения, которые мешают зарабатывать на вапклике 
 



- на подписках не заработать!
 Деньги любят тишину. Те, кто зарабатывает стабильно и много, об этом не говорят на каждом углу. Каждый знает, чем меньше конкуренция, тем больше личных возможностей. Представьте себе сколько денег там, куда никто не сунет нос просто потому, что все вокруг передают информацию о «невыигрышности» вап-клика. 
Суперафиляты, зарабатывающие исключительно на подписках и партнерки работают в выгодном тандеме, но места на этом рынке и достойного дохода хватит всем. Если вы сомневаетесь, вы можете прочитать кейс – пример того, что деньги в вапе есть – и еще какие!

- нужны большие бюджеты на тесты
Задайте себе вопрос: «Что для меня большой бюджет?». Если 10к рублей – это много, то сложно представить, куда вообще вам можно будет успешно зайти. Найти проверенную сетку хотя бы по отзывам в Интернете, засплитить 5 офферов, в течение дня оптимизируя кампанию – достаточно, чтобы на следующий день выйти хоть в небольшой, но плюс. Небольшого бюджета хватит.

- нет нормальных источников трафика: везде боты
Мы уже писали о необходимом инструменте – трекере. Он создан не только для того, чтобы смотреть на РОИ, но и для настоящих самураев, умеющих пользоваться катаной – боторезалкой. В основном трекеры сами умеют фильтровать ботов в однокнопочном режиме, но если этого недостаточно и бот – это настолько страшно, то вы вольны поставить собственные параметры для отсечения ненужного трафика. Вы ведь знаете, на кого льете? Создайте фильтр по геолокации, IP – диапазонами, порежьте трафик по юзер – агентам.

Представим, что вы покупаете российский операторский трафик, Билайн и хотите отсечь ненужный трафик «плохих» и «хороших» ботов. Вам не хватает галочки в чекбоксе для отсечения. Тогда необходимо создать правило в трекере. Самое простое – бот не будет подходить под гео – Россия, создаете правило «Country IS NOT Russia» ( для Binom ). По такому же принципу создаем фильтр для заголовков, бот обычно передает такие: com.android.chrome, com.facebook, ru.yandex и тд. ( полные списки вы найдете в интернете ), а также отсутствующий или пустой заголовок. Также рекомендуем отсекать подписки с утройств Meizu, выставлять свои IP – листы в источниках трафика.

