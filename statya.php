<b>Что происходит на рынке мобильных подписок?</b>
<br>
 <br>
Ну что ж, для сомнений: “а стоит ли работать с вапкликом?”, есть несколько поводов.  В период 2018-2019 на рынке мобильных подписок произошло большое количество значимых событий:

   1. 	Ушли с рынка по разным причинам заметные игроки:  РГК, Shakes.im, Stable Money, Moblave
2. 	Закрыты адалт-сервисы, в их привычном виде, оператором МТС. Оператор изменил требования к лендингам, в результате  сильно упал конверт. 
3. 	Изменилась модель работы Билайна:  произошел перевод адалт-сервисов в так называемую «агентскую» модель и, как это нередко происходит, снижение отчислений почти на 30%.
4. 	Изменились алгоритмы и критерии оценки качества трафика МТС

Все это изменило условия работы с подписками, в какой-то мере сделало сложнее, но при этом остались возможности достойного заработка. 
 
Одна из таких возможностей  –   работа по CPA модели (выкуп). Здесь есть свои нюансы, о которых мы расскажем ниже. Основные же преимущества для арбитражника: простота и быстрая окупаемость. 
 
Как лить в плюс в нынешних условиях?  
 
Базовые, необходимые для старта, шаги:
1 Выбираем трекеры. На рынке множество вариантов и все решения в той или иной степени способны работать с подписочным трафиком. Субъективно оптимальным вариантом видится Бином, как наиболее удобный инструмент именно для подписок.  С помощью трекера можно достаточно просто отсекать ботов, мануал вы найдете ниже. 

2 Выбираем источник трафика. Традиционные источники трафика на подписки - тизерные сети. Вот краткий список:
VisitWeb, Teasernet,  MPAY, BigClick, BuyMedia
Если вы работаете с AffiMob, то можете получить дополнительный бонус 5% при переводе баланса из кабинета на аккаунт тизерной сети. Также при этом вы не платите комиссию на пополнение баланса, что неплохо экономит бюджет. 

3 Выбираем партнерку 

 
Базовые критерии для выбора партнерки:
прозрачность/понятность статистики
 регулярность и вариативность выплаты (wmz, paypal, capitalist, и т.д.)
 оперативность и полезность саппорта. Регистрация должна подтверждаться днем не более 1 часа, ночью – часов за 6.
 
Получить живые отзывы о партнерке всегда можно в любом из профильных чатов в телеграм. Там можно задать вопрос и, пройдя через стену троллинга и шутеек, получить ответ от одной из партнерок. Может быть сразу в ЛС.
 
Плюсом партнерки будет являться наличие прямых договоров с операторами. Ближе к кассе, так сказать. Потраченные абонентами деньги платит оператор. Соответственно – чем меньше промежуточных звеньев в денежно-пищевой цепи, тем больше получит арбитражник. «Свою» партнерку надо будет поискать, это не всегда происходит быстро.
 
  
Нюансы при выборе партнерки
 
 
Если сравнить ставки выкупа, то  несложно обнаружить разные цифры. Например,  по Темафону (провайдер сервисов для абонентов Билайна) ставки варьируются от 18 до 30 руб. за подписку. При этом почти все ПП пишут про «моментальный» выкуп. 
На практике это не совсем и не всегда так. ПП может не выкупать все подписки и на это есть две причины:
1. 	Холд при выкупе. По статистике до 15% подписчиков отписывается в первые 2 минуты. Таким образом ПП, поставив холд на выкуп 120 секунд, просто не будет выкупать часть привлеченных вами подписок. На практике холд может быть значительно длиннее.
2. 	У многих партнерок действует выкуп подписок только если они соответствуют заданным параметрам (IP+UA+OS). А это также снижает % выкупаемых подписок 

Ничего криминального в таких инструментах со стороны ПП нет: выкупать подписки в минус – это так себе бизнес, мягко говоря. Но есть нюанс:  далеко не все партнерки прозрачно и однозначно обозначают условия выкупа на старте, завышают ставки, чтобы условия выкупа выглядели привлекательно,  а в реальности партнер может получать меньше чем у конкурентов. 

 
Выводы – не ведитесь на размер ставки и задавайте больше вопросов саппорту ПП до начала работы. Решение о начале тестов принимайте после получения ответов на ваши вопросы. 
 
